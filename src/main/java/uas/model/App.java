package uas.model;

/**
 * 应用
 * 
 * @author percy
 *
 */
public class App {

	private String id;
	/**
	 * 应用名称
	 */
	private String name;
	/**
	 * 应用编码
	 */
	private String code;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
